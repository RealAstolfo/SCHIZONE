align 16

log_10:
; double {xmm0} log_10(double {xmm0}, double {xmm1});
;	Computes Taylor Series approximation of log({xmm0}) (base-10) to
;	within tolerance {xmm1}/ln(10), returning in {xmm0}.

	; if x<=0, return NaN
	; otherwise bisect thru lookup table for a matched power of 10
	; otherwise compute via log_e({xmm0})

	comisd xmm0,[.lookup_table] ; check for NaN results
	jbe .ret_NaN

	push rax
	push rcx
	push rdx

	xor rax,rax	; range lower bound 0
	mov rdx,634	; range upper bound 634

.bisection_loop:	; bisect thru lookup table in case of a power of 10
	mov rcx,rax
	add rcx,rdx
	shr rcx,1
	cmp rcx,rax
	je .converged
	comisd xmm0,[.lookup_table+8*rcx]
	ja .shrink_up
	jb .shrink_down
.converged:
	; down to just 2 options
	comisd xmm0,[.lookup_table+8*rax]
	jne .not_rax
	sub rax,324
	cvtsi2sd xmm0,rax
	jmp .pop
.not_rax:
	comisd xmm0,[.lookup_table+8*rdx]
	jne .not_found
	sub rdx,324
	cvtsi2sd xmm0,rdx
	jmp .pop

.shrink_up:
	mov rax,rcx
	jmp .bisection_loop

.shrink_down:
	mov rdx,rcx
	jmp .bisection_loop

.not_found:
	call log_e
	mulsd xmm0,[.ln_ten_inv]; {xmm0}=ln({xmm0})/ln(10)
.pop:
	pop rdx
	pop rcx
	pop rax

	ret

.ret_NaN:
	movsd xmm0,[.NaN]
	ret

